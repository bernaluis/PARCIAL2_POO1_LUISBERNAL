    private void dgvBodegaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dgvBodegaMouseClicked
        // TODO add your handling code here:
        int fila = this.dgvBodega.getSelectedRow();
        if (fila >= 0) {
            Bodega obje = (Bodega) this.dgvBodega.getValueAt(fila, 0);
            this.codi=obje.getCodigo();
            this.codigop=obje.getCodigo_prov().getCodigo();
            this.codigopi=obje.getCodigo_pieza().getCodigo();
            //se deja editable porque el setselecteditem es editar
            this.cmbPiezas.setEditable(true);
            this.cmbPiezas.setSelectedItem(obje.getCodigo_pieza());
            this.cmbPiezas.setEditable(false);
            this.cmbProveedores.setEditable(true);
            this.cmbProveedores.setSelectedItem(obje.getCodigo_prov());
            this.cmbProveedores.setEditable(false);
            this.txtCantidad.setText(String.valueOf(obje.getCantidad()));
            this.txtFecha.setText(new SimpleDateFormat("dd-MM-yyyy").format(obje.getFecha()));
          
        }
    }//GEN-LAST:event_dgvBodegaMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Estas seguro que deseas borrarlo?", "Alerta", JOptionPane.YES_NO_OPTION);
        if (confirmacion == JOptionPane.YES_OPTION) {
            if (new Bodega_Ctrl().del(this.codi)) {
                JOptionPane.showMessageDialog(null, "Elimino", "Exito", JOptionPane.INFORMATION_MESSAGE);
                this.llenarTabla();

            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

