     private void cargarComboPiezas() {
        DefaultComboBoxModel<Piezas> modeJ = new DefaultComboBoxModel<>();
        modeJ.removeAllElements();
        for (Piezas temp : new Piezas_Ctrl().consTodo()) {
            modeJ.addElement(temp);
        }
        this.cmbPiezas.setModel((DefaultComboBoxModel) modeJ);
    }
    private void cargarComboProveedores() {
        DefaultComboBoxModel<Proveedores> modeH = new DefaultComboBoxModel<>();
        modeH.removeAllElements();
        for (Proveedores temp : new Proveedores_Ctrl().consTodo()) {
            modeH.addElement(temp);
        }
        this.cmbProveedores.setModel((DefaultComboBoxModel) modeH);
    }
     private void llenarTabla() {
        try {
            DefaultTableModel model = (DefaultTableModel) this.dgvBodega.getModel();

            while (model.getRowCount() > 0) {
                model.removeRow(0);

            } //Limpiar modelo
            for (Bodega temp : new Bodega_Ctrl().consTodo()) {
                model.addRow(new Object[]{temp,temp.getCodigo_prov().getNombre(),temp.getCantidad(),new SimpleDateFormat("dd-MM-yyyy").format(temp.getFecha())});
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
